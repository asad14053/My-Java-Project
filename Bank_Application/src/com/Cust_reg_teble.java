    public ArrayList<Custom_class> getUserList() {
        ArrayList<Custom_class> usersList = new ArrayList<Custom_class>();

        Connection connection = getConnection();
        conn = JavaConnectDb.ConnecrDb();
        String query = "select * from ct_reg";
        Statement st;
        ResultSet rs;

        try {
            String driverName = "oracle.jdbc.driver.OracleDriver";
            Class.forName(driverName);

            st = connection.createStatement();
            rs = st.executeQuery(query);
            Custom_class user;
            while (rs.next()) {
                user = new Custom_class(rs.getString("name"), rs.getString("id"), rs.getString("address"), rs.getString("acc_type"), rs.getString("sex"), rs.getString("occupation"), rs.getString("password"), rs.getString("acc_num"), rs.getDate("dob"), rs.getInt("balance"));
                usersList.add(user);
            }
        } catch (Exception e) {
            e.printStackTrace();

        }
        return usersList;
    }

    public void staff_reg_table() {
        ArrayList<Custom_class> list = getUserList();

        DefaultTableModel model;
        model = (DefaultTableModel) jTable1.getModel();

        Object[] row;
        row = new Object[10];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getname();
            row[1] = list.get(i).getId();
            row[2] = list.get(i).getaddress();
            row[3] = list.get(i).getacc_num();
            row[4] = list.get(i).getsex();
            row[5] = list.get(i).getoccupation();
            row[6] = list.get(i).getpassword();
            row[7] = list.get(i).getacc_num();
            row[8] = list.get(i).getdob();
            row[9] = list.get(i).get_balance();

            model.addRow(row);
        }
    }

    public void executeSQLQuery(String query, String messege) {
        Connection con = getConnection();
        Statement st;
        try {
            st = con.createStatement();
            if ((st.executeUpdate(query)) == 1) {
                //refresh jtable data
                DefaultTableModel model;
                model = (DefaultTableModel) jTable1.getModel();
                model.setRowCount(0);
                staff_reg_table();
                JOptionPane.showMessageDialog(null, "Data " + messege + "succesfully");
            } else {
                JOptionPane.showMessageDialog(null, "Data Not " + messege);
            }

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

